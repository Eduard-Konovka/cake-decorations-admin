export default function titleWrapper(language, obj) {
  switch (language) {
    case 'UA':
      return obj?.title?.ua ?? obj?.title?.ru ?? obj?.title?.en ?? '';

    case 'RU':
      return obj?.title?.ru ?? obj?.title?.ua ?? obj?.title?.en ?? '';

    case 'EN':
      return obj?.title?.en ?? obj?.title?.ua ?? obj?.title?.ru ?? '';

    default:
      return obj?.title?.ua ?? '';
  }
}
